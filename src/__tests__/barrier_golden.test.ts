// T3 Crash Shield (Barrier) golden-CBOR regression.
//
// T3 needs NO new composer — buildUnderwriteParts already supports
// riskClass:'Barrier'. This suite LOCKS the Barrier underwrite path byte-for-byte
// for the three canonical crash-shield feeds:
//
//   ADA   → FEEDS.ADA_USD (f0f14cd0…342301)  — AegisSelf publisher feed
//   iBTC  → FEEDS.BTC_USD (99e8fe4f…2163b3)  — AegisSelf publisher feed
//   iETH  → FEEDS.ETH_USD (a8c5354a…95f9c)   — AegisSelf publisher feed
//
// Each asserts the exact policy inline datum (with that feed's oracle NFT +
// oracleProvider AegisSelf + riskClass Barrier), the pool continuation datum,
// the pool/marker redeemers, and validity. The three policy datums differ ONLY
// in the oracle-NFT field, proving the feed wiring is the single moving part.
//
// The mainnet AegisSelf spot feeds (ADA/BTC/ETH) are the canonical publisher
// NFTs (constants.mainnet.AEGIS_PUBLISHER_CANONICAL_NFTS), so the correct
// oracleProvider for every one of them is 'AegisSelf' (the composer default) and
// the oracle NFT is the feed's policyId. A Barrier policy REQUIRES an oracle ref
// input on chain — `references.oracleRequired` must be true.
//
// iSOL / SOL is intentionally ABSENT: there is no canonical mainnet SOL feed in
// FEEDS or AEGIS_PUBLISHER_CANONICAL_NFTS (iSOL exists only as an Indigo CDP
// mock with a placeholder "MOCK_ISOL_ORACLE", not a published NFT). It is OUT OF
// SCOPE until a real feed is published — see the dedicated test below. We do NOT
// invent a policy id.

