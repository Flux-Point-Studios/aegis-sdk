// Pool continuation + redeemers are feed-independent (same coverage/premium).
const POOL_DATUM_GOLDEN =
  'd8799f1b00000017bd4a22001ab2d05e00581c5cb64f303517777710d28db50ad3be4bb9feda5f66d0fbffa68e212b18c8581c9a649b75a85f0088eb68c1b72c3529b41f874fcdc603031a1444abb31b0000000ba43b7400ff';
const POOL_REDEEMER_GOLDEN = 'd8799f1a773594001a773593ffff';
const MARKER_REDEEMER_GOLDEN = 'd8799f01ff';

const FEED_CASES: { name: string; feed: typeof FEEDS.ADA_USD }[] = [
  { name: 'ADA (FEEDS.ADA_USD)', feed: FEEDS.ADA_USD },
  { name: 'iBTC (FEEDS.BTC_USD)', feed: FEEDS.BTC_USD },
  { name: 'iETH (FEEDS.ETH_USD)', feed: FEEDS.ETH_USD },
];

describe.each(FEED_CASES)('T3 Crash Shield Barrier golden — $name', ({ feed }) => {
  const parts = barrierParts(feed.policyId);

  it('is insurable and carries the Barrier risk class', () => {
    expect(parts.insurable).toBe(true);
    expect(parts.policyDatum.riskClass).toBe('Barrier');
  });

  it('pins the policy to this feed NFT with the AegisSelf provider', () => {
    // The feed registry resolves the right NFT, and the publisher feed is
    // AegisSelf-published (constants.mainnet AEGIS_PUBLISHER_CANONICAL_NFTS).
    expect(feed.kind).toBe('spot');
    expect(feed.riskClass).toBe('Barrier');
    expect(findFeedByPolicyId(feed.policyId)).toBe(feed);
    expect(bytesToHex(parts.policyDatum.oracleNft)).toBe(feed.policyId);
    expect(parts.policyDatum.oracleProvider).toBe('AegisSelf');
  });

  it('GOLDEN: policy output datum CBOR (byte-for-byte, only the oracle NFT varies)', () => {
    expect(parts.policyOutput.inlineDatumCbor).toBe(POLICY_DATUM[feed.symbol]);
    // Provider AegisSelf = Constr 2 (tag 123 → d87b80); Barrier = Constr 0 (d87980).
    expect(parts.policyOutput.inlineDatumCbor).toContain('d87b80'); // AegisSelf
    expect(parts.policyOutput.inlineDatumCbor.endsWith('d87a8000d87980ff')).toBe(true); // None, 0, Barrier
  });

  it('places coverage + 1 marker at the policy script address', () => {
    expect(parts.policyOutput.address).toBe(POLICY_ADDR);
    expect(parts.policyOutput.lovelace).toBe(COVERAGE);
    expect(parts.policyOutput.marker).toEqual({ policyId: MARKER_HASH, assetNameHex: MARKER_NAME_HEX, quantity: 1n });
  });

  it('GOLDEN: pool continuation datum CBOR (total += net_growth, active += coverage)', () => {
    expect(parts.poolOutput.address).toBe(POOL_ADDR);
    expect(parts.poolOutput.inlineDatumCbor).toBe(POOL_DATUM_GOLDEN);
    expect(parts.poolOutput.poolNft).toEqual({
      policyId: POOL_NFT_POLICY,
      assetNameHex: bytesToHex(new TextEncoder().encode('AEGIS_POOL_V4')),
      quantity: 1n,
    });
  });

  it('GOLDEN: pool redeemer = Underwrite{coverage, premium} + marker MintMarkers{1}', () => {
    expect(parts.poolRedeemerCbor).toBe(POOL_REDEEMER_GOLDEN);
    expect(decodePoolRedeemer(hexToBytes(parts.poolRedeemerCbor))).toEqual({
      kind: 'Underwrite',
      coverage: COVERAGE,
      premium: PREMIUM,
    });
    expect(parts.mint.redeemerCbor).toBe(MARKER_REDEEMER_GOLDEN);
    expect(decodeMarkerRedeemer(hexToBytes(parts.mint.redeemerCbor))).toEqual({ kind: 'MintMarkers', count: 1 });
  });

  it('a Barrier policy REQUIRES the oracle reference input on chain', () => {
    expect(parts.references.oracleRequired).toBe(true);
  });

  it('validity: start + 30d expiry are exactly the pinned times', () => {
    expect(parts.validity.startTimeMs).toBe(START);
    expect(parts.validity.expiryTimeMs).toBe(EXPIRY);
  });
});

describe('T3 Barrier golden — the three policy datums differ ONLY in the oracle NFT', () => {
  it('ADA / iBTC / iETH datums are byte-identical except the 581c<NFT> field', () => {
    const ada = POLICY_DATUM.ADA_USD;
    const btc = POLICY_DATUM.BTC_USD;
    const eth = POLICY_DATUM.ETH_USD;
    // Replace each feed NFT with a placeholder; the remaining bytes must match.
    const norm = (s: string, nft: string) => s.replace(nft, '<NFT>');
    expect(norm(ada, FEEDS.ADA_USD.policyId)).toBe(norm(btc, FEEDS.BTC_USD.policyId));
    expect(norm(btc, FEEDS.BTC_USD.policyId)).toBe(norm(eth, FEEDS.ETH_USD.policyId));
  });
});

// ───────────────────────────────────────────────────────────────────────────
// quoteForPosition — Barrier insurability boundary (strike ≥15% below spot).
// ───────────────────────────────────────────────────────────────────────────

describe('quoteForPosition Barrier — 15%-below-spot insurable boundary', () => {
  const SPOT_1 = 1_000_000n; // $1.00
  const base = {
    riskClass: 'Barrier' as const,
    coverageLovelace: 2_000_000_000n,
    spotPriceScaled: SPOT_1,
    durationDays: 30,
  };

  it('exactly 15% below spot PASSES (dBps == MIN_STRIKE_DISTANCE_BPS)', () => {
    // strike $0.85 → d = (1.00-0.85)/1.00 = 15.00% = 1500 bps = the minimum.
    const v = quoteForPosition({ ...base, strikePriceScaled: 850_000n });
    expect(v.dBps).toBe(MIN_STRIKE_DISTANCE_BPS); // 1500
    expect(v.insurable).toBe(true);
    expect(v.reasonCode).toBeNull();
  });

  it('14.9% below spot is REJECTED with BELOW_MIN_STRIKE_DISTANCE', () => {
    // strike $0.851 → d = 14.9% = 1490 bps < 1500.
    const v = quoteForPosition({ ...base, strikePriceScaled: 851_000n });
    expect(v.dBps).toBe(1490);
    expect(v.insurable).toBe(false);
    expect(v.reasonCode).toBe('BELOW_MIN_STRIKE_DISTANCE');
    expect(v.reason).toMatch(/15% minimum|deeper strike/i);
  });

  it('one bps inside the boundary (14.99% via $0.8501) is still rejected', () => {
    // strike $0.8501 → d = 1499 bps < 1500 (boundary is closed at 1500).
    const v = quoteForPosition({ ...base, strikePriceScaled: 850_100n });
    expect(v.dBps).toBe(1499);
    expect(v.insurable).toBe(false);
    expect(v.reasonCode).toBe('BELOW_MIN_STRIKE_DISTANCE');
  });

  it('a strike AT or ABOVE spot is rejected as STRIKE_NOT_BELOW_SPOT', () => {
    const atSpot = quoteForPosition({ ...base, strikePriceScaled: SPOT_1 });
    expect(atSpot.insurable).toBe(false);
    expect(atSpot.reasonCode).toBe('STRIKE_NOT_BELOW_SPOT');

    const aboveSpot = quoteForPosition({ ...base, strikePriceScaled: 1_100_000n });
    expect(aboveSpot.insurable).toBe(false);
    expect(aboveSpot.reasonCode).toBe('STRIKE_NOT_BELOW_SPOT');
  });
});

